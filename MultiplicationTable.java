package lab01;

public class MultiplicationTable {
    public static void main(String[] args) {
        int number = 2;
        
        System.out.println("Multiplication Table for " + number + ":");
        
        for (int i = 1; i <= 10; i++) {
            System.out.println(number + " x " + i + " = " + (number * i));
            
        }
        System.out.println("Hana wasi1");
    }
}
